<template>
    <div>
         <!-- 面包屑区域 -->
        <el-breadcrumb separator-class="el-icon-arrow-right">
            <el-breadcrumb-item :to="{ path: '/home' }">首页</el-breadcrumb-item>
            <el-breadcrumb-item>订单管理</el-breadcrumb-item>
            <el-breadcrumb-item>订单列表</el-breadcrumb-item>
        </el-breadcrumb>

        <!-- 卡片区域 -->
        <el-card>
            <el-row>
                <el-col :span="8">
                    <el-input placeholder="请输入内容">
                        <el-button slot="append" icon="el-icon-search"></el-button>
                    </el-input>
                </el-col>
            </el-row>
            <!-- 订单列表页面 -->
            <el-table :data="ordersList" border stripe>
                <el-table-column type="index"></el-table-column>
                <el-table-column label="订单编号" prop="order_number"></el-table-column>
                <el-table-column label="订单价格" prop="order_price"></el-table-column>
                <el-table-column label="是否付款" prop="pay_status">
                    <template slot-scope="scope">
                        <el-tag type="danger" v-if="scope.row.pay_status == '0' ">未付款</el-tag>
                        <el-tag type="success" v-else>已付款</el-tag>
                    </template>
                </el-table-column>
                <el-table-column label="是否发货" prop="is_send"></el-table-column>
                <el-table-column label="下单时间" prop="create_time">
                    <template slot-scope="scope">
                        {{ scope.row.create_time | dateFormat }}
                    </template>
                </el-table-column>
                <el-table-column label="操作">
                    <template slot-scope="scope">
                        <el-button size="mini" type="primary" icon="el-icon-edit" @click="showBox"></el-button>
                        <el-button size="mini" type="success" icon="el-icon-location"></el-button>
                    </template>
                </el-table-column>
            </el-table>
            <!-- 分页相关 -->
            <el-pagination
                @size-change="handleSizeChange"
                @current-change="handleCurrentChange"
                :current-page="queryInfo.pagenum"
                :page-sizes="[1, 5, 10, 20]"
                :page-size="queryInfo.pagesize"
                layout="total, sizes, prev, pager, next, jumper"
                :total="total">
            </el-pagination>
        </el-card>
        <!-- 修改地址对话框 -->
        <el-dialog
            title="修改地址"
            :visible.sync="addressVisible"
            width="50%"
            @close= "addressDialogClosed"
            >
            <el-form :model="addressForm" :rules="addressFormrRules" ref="addressFormrRef" label-width="100px" class="demo-ruleForm">
                <el-form-item label="省市区/县" prop="address1">
                    <!-- <el-input v-model="addressForm.address1"></el-input> -->
                    
                    <el-cascader
                        v-model="addressForm.address1"
                        :options="cityOptions"
                        :props="cascaderProps"
                        @change="handleChange">
                    </el-cascader>
                </el-form-item>
                <el-form-item label="详细地址" prop="address2">
                    <el-input v-model="addressForm.address2"></el-input>
                </el-form-item>
            </el-form>
            <span slot="footer" class="dialog-footer">
                <el-button @click="addressVisible = false">取 消</el-button>
                <el-button type="primary" @click="addressVisible = false">确 定</el-button>
            </span>
        </el-dialog>
    </div>
</template>

<script>
import cityOptions from './city_data2017_element'
export default {
    data() {
        return {
            queryInfo: {
                query: '',
                pagenum: 1,
                pagesize: 10
            },
            total: 0,
            ordersList: [],
            addressVisible: false,
            addressForm: {
                address1: [],
                address2: ''
            },
            addressFormrRules: {
                address1: [
                    {required: true, message: '请选择省市区/县', trigger: 'blur'}
                ],
                address2: [
                    {required: true, message: '请填写详细地址', trigger: 'blur'}
                ]
            },
            // selectedValue: [],
            cascaderProps: {
                expandTrigger:'hover',
                value: 'value',
                label: 'label',
                children: 'children'
                // checkStrictly: true
            },
            cityOptions: cityOptions

        }
    },
    methods: {
        async getOrderList() {
            const { data: res} = await this.$http.get('orders', {
                params: this.queryInfo  
            })
            if(res.meta.status !== 200) return this.$message.error('获取订单列表出错')
            this.ordersList = res.data.goods
            this.total = res.data.total
            // console.log(res.data)
        },
        handleSizeChange(newSize) {
            this.queryInfo.pagesize = newSize
            this.getOrderList()
        },
        handleCurrentChange(newPage) {
            this.queryInfo.pagenum = newPage
            this.getOrderList()
        },
        showBox() {
            this.addressVisible = true

        },
        //级联选择器发生变化
        handleChange() {},
        addressDialogClosed() {
            this.$refs.addressFormrRef.resetFields()
        }

    },
    created() {
        this.getOrderList()
    }
}
</script>

<style lang="less" scoped>
.el-cascader {
    width: 100%;
}
